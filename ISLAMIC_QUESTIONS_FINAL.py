def get_all_questions() -> List[Dict]:
    """الحصول على جميع الأسئلة"""
    all_questions = []
    for category in ISLAMIC_QUESTIONS.values():
        for difficulty in category.values():
            all_questions.extend(difficulty)
    return all_questions

def get_questions_by_category(category: str) -> List[Dict]:
    """الحصول على أسئلة حسب التصنيف"""
    if category not in ISLAMIC_QUESTIONS:
        return []
    
    cat_questions = []
    for difficulty in ISLAMIC_QUESTIONS[category].values():
        cat_questions.extend(difficulty)
    return cat_questions

def get_questions_by_difficulty(difficulty: str) -> List[Dict]:
    """الحصول على أسئلة حسب مستوى الصعوبة"""
    if difficulty not in ["سهل", "متوسط", "صعب"]:
        return []
    
    difficulty_questions = []
    for category in ISLAMIC_QUESTIONS.values():
        if difficulty in category:
            difficulty_questions.extend(category[difficulty])
    return difficulty_questions

def get_questions_by_category_and_difficulty(category: str, difficulty: str) -> List[Dict]:
    """الحصول على أسئلة محددة"""
    if category not in ISLAMIC_QUESTIONS:
        return []
    if difficulty not in ISLAMIC_QUESTIONS[category]:
        return []
    
    return ISLAMIC_QUESTIONS[category][difficulty]

def get_random_questions(
    count: int = 10, 
    category: Optional[str] = None,
    difficulty: Optional[str] = None
) -> List[Dict]:
    """الحصول على أسئلة عشوائية"""
    if category and difficulty:
        questions = get_questions_by_category_and_difficulty(category, difficulty)
    elif category:
        questions = get_questions_by_category(category)
    elif difficulty:
        questions = get_questions_by_difficulty(difficulty)
    else:
        questions = get_all_questions()
    
    if not questions:
        return []
    
    return random.sample(questions, min(count, len(questions)))

def get_mixed_difficulty_questions(
    easy: int = 5,
    medium: int = 3,
    hard: int = 2,
    category: Optional[str] = None
) -> List[Dict]:
    """الحصول على أسئلة مختلطة"""
    questions = []
    
    if category:
        if category not in ISLAMIC_QUESTIONS:
            return []
        
        cat_data = ISLAMIC_QUESTIONS[category]
        questions.extend(random.sample(cat_data["سهل"], min(easy, len(cat_data["سهل"]))))
        questions.extend(random.sample(cat_data["متوسط"], min(medium, len(cat_data["متوسط"]))))
        questions.extend(random.sample(cat_data["صعب"], min(hard, len(cat_data["صعب"]))))
    else:
        questions.extend(get_random_questions(easy, difficulty="سهل"))
        questions.extend(get_random_questions(medium, difficulty="متوسط"))
        questions.extend(get_random_questions(hard, difficulty="صعب"))
    
    random.shuffle(questions)
    return questions

def get_category_statistics() -> Dict:
    """الحصول على إحصائيات"""
    stats = {
        "total_questions": 0,
        "categories": {},
        "difficulties": {"سهل": 0, "متوسط": 0, "صعب": 0}
    }
    
    for category, cat_data in ISLAMIC_QUESTIONS.items():
        cat_count = sum(len(questions) for questions in cat_data.values())
        stats["categories"][category] = {
            "total": cat_count,
            "سهل": len(cat_data["سهل"]),
            "متوسط": len(cat_data["متوسط"]),
            "صعب": len(cat_data["صعب"])
        }
        stats["total_questions"] += cat_count
        
        for difficulty in ["سهل", "متوسط", "صعب"]:
            stats["difficulties"][difficulty] += len(cat_data[difficulty])
    
    return stats

